// Faz uma contagem regressiva de 10 até 1 usando um loop for

// Início do loop:
// let i = 10 → começa a contagem em 10
// i > 0 → continua enquanto i for maior que 0
// i-- → decrementa 1 a cada repetição
for (let i = 10; i > 0; i--) {
    // Mostra o valor atual de i no console
    console.log(i);
}